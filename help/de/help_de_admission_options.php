<b>Optionen bei der Aufnahme</b></font>

<p><font size=2 face="verdana,arial" >

<font color="#990000"><b>
Arbeitsunfähigkeits-(AU-)Bestätigung</b></font>
<ul>
Formulare erzeugen oder anzeigen lassen.<p>
</ul>

<font color="#990000"><b>
Kurvenblätter</b></font>
<ul>
Öffnet den Ordner mit den:
<li> Temperaturkurven
<li> Pflegeberichten
<li> ärztlichen Anordnungen
<li> Diagnostischen Berichten
<li> DRG Modulen
<li> Laborbefunden
<li> Bildern
<li> Untersuchungsanforderungen
</ul>

<font color="#990000"><b>
Diagnostische Befunde</b></font>
<ul>
Listet alle für diesen Aufenthalt verfügbaren diagnostischen Befunde/Berichte auf.
</ul>

<font color="#990000"><b>
Medocs</b></font>
<ul>
Öffnet das Medocs-Modul für diesen Aufenthalt.
</ul>

<font color="#990000"><b>
DRG (composite)</b></font>
<ul>
Öffnet das DRG-Modul für diesen Aufenthalt.
</ul>

<font color="#990000"><b>Anordnungen</b></font>
<ul>
Ansehen oder Eintragen von Anordnungen.
</ul>

<font color="#990000"><b>Berichte und Anmerkungen</b></font>
<ul>
Ansehen oder Eintragen verschiedener Anmerkugnen und Berichte. Z.B. die Krankengeschichte oder
den Entlassungsbericht.
</ul>

<font color="#990000"><b>Impfungen</b></font>
<ul>
Ansehen oder Eintragen von Impfungen.
</ul>

<font color="#990000"><b>Mess-Daten</b></font>
<ul>
Ansehen oder Eintragen von gemessenen Grössen wie Gewicht, Größe, Kopfumfang...
</ul>

<font color="#990000"><b>Schwangerschaften</b></font>
<ul>
Ansehen oder Eintragen von Schwangerschaftsdaten (bei Frauen).
</ul>

<font color="#990000"><b>Geburts-Details</b></font>
<ul>
Ansehen oder Eintragen von Geburts-Details.
</ul>

<font color="#990000"><b>Personen-Daten anzeigen</b></font>
<ul>
Zeigt die Personen-Daten an.
</ul>

<font color="#990000"><b>Personen-Daten ändern</b></font>
<ul>
Ändern der Personen-Daten.
</ul>

<font color="#990000"><b>Datensatz-History</b></font>
<ul>
Pop-up Fenster, das die History des Aufnahme-Datensatzes in der Datenbank anzeigt.
</ul>

<font color="#990000"><b>Aufnahme abbrechen</b></font>
<ul>
Bricht die Aufnahme ab. Nur möglich, wenn der Patient noch nicht auf einer Station
oder Klinik aufgenommen ist.
</ul>

