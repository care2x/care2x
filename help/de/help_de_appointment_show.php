<p><font color="#990000">Wie kann ich mir Termine anzeigen lassen?</font></b><p>
<font face="Verdana, Arial" size=2>
<ul>
Voreingestellt ist, dass die Termine des heutigen Tages angezeigt werden.
</ul>

<font face="Verdana, Arial" size=3>
<b>
<p><font color="#990000">Wie kann ich die Termine eines anderen Tages anschauen?</font></b><p>
<font face="Verdana, Arial" size=2>
<ul>
Klicken Sie einfach auf das entsprechende Datum im Mini-Kalender.
<p>
<img src="../help/en/img/en_minicalendar.png" border=0 width=230 height=136>
</ul>

<font face="Verdana, Arial" size=3>
<b>
<p><font color="#990000">Wie kann ich die Termine einer bestimmten Abteiung anzeigen lassen?</font></b><p>
<font face="Verdana, Arial" size=2>
<ul>
Wählen Sie mit Hilfe des Auswahl-Buttons die entsprechene Abteilung aus und klicken Sie
auf "<font color=#ff0000>Anzeigen</font>".
<p>
<img src="../help/en/img/en_deptselect.png" border=0 width=306 height=234></ul>


<font face="Verdana, Arial" size=3>
<b>
<p><font color="#990000">Wie kann ich die Termine eines bestimmten Arztes ansehen?</font></b><p>
<font face="Verdana, Arial" size=2>
<ul>
Geben Sie das Suchkürzel des entsprechenden Arztes an und klicken Sie auf "<font color=#ff0000>Anzeigen</font>";
<p>
<img src="../help/en/img/en_doctorselect.png" border=0 width=308 height=157>
</ul>
</font>

