<font face="Verdana, Arial" size=2>
Care 2X ist zu 100% Web-Browser basiert. Sie können es genauso benutzen wie Sie im
Internet surfen. <br> Das Hauptfenster ist wie folgt in 3 Blöcke aufgeteilt:
<blockquote>
<li>Block A = Das Hauptmenü. Fast jede Aktion beginnt hier.
<li>Block B = In diesem Bereich wird gearbeitet oder eine Funktion ausgewählt.
<li>Block C = Hier werden der Name des Moduls oder der Funktion sowie Buttons angezeigt.
<p>
<img src="img/en_mainframe.png">
</blockquote>

<b>Eine Funktion aufrufen</b><p>
<blockquote>
Klicken Sie einfach auf den Namen der Funktion<br>
<img src="img/en_linkclick.png">
</blockquote>

<b>Auswahl der Sprache</b><p>
<blockquote>
<li>Klicken Sie auf den Auswahlknopf für Sprachen unten links.
<li>Klicken Sie auf die gewünschte Sprache.
<li>Klicken Sie auf den "Wechseln"-Button.
<img src="img/en_lang_select.png">
</blockquote>

<b>Authentifizieren Sie sich </b></b><p>
<blockquote>
<li>Geben Sie Ihren Benutzernamen ein.
<li>Geben Sie Ihr Passwort ein.
<li>Klicken Sie auf den "Weiter..."-Button.<p>
<img src="img/en_login.png">
</blockquote>
