Wie ändere ich die Hauptdiagnose?</b></font>
<ul>
	<b>Schritt 1: </b>Klicken Sie auf die Auswahlbox der (neu gewählten) Hauptdiagnose.<p>
	<b>Schritt 2: </b>Wählen Sie die Kategorie "Hauptdiagnose" druch Anklicken aus.
	Diese Diagnose wird ab diesem Zeitpunkt zur Hauptdiagnose.<p>
</ul>

