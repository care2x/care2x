<font face="Verdana, Arial" size=3 color="maroon"><b>How to use the online manual</b></font>
<p>
<font face="Verdana, Arial" size=2>
Das Online-Handbuch ist internetbasiert. Sie können nur darauf zugreifen, wenn Ihr System mit dem Internet verbunden ist
und Ihre Firewall diesen Zugriff erlaubt.<p>
<b>Wie öffne ich das Online-Handbuch?</b>
<blockquote>
Durch Klick auf "Online manual" im Menü links.
</blockquote>
<p>
<b>Wie finde ich schnell was ich suche?</b>
<blockquote>
<li>Benutzen Sie das Suchformular auf der "FindPage"-Seite. Klicken Sie dazu auf den Link "FindPage" unten links auf der Seite.
<p>
<img src="img/en_online_findpage.png"><p>
<li>Sie können entweder in den Seitentiteln (entspricht so etwa Kapiteln) suchen (oberes Sucheingabefeld der FindPage-Seite) oder
<li>Sie suchen im Volltext der Hilfeseiten: Geben Sie hierzu ein passendes Schlüsselwort Ihrer Frage ein (Z.B. "tägliche Diät") im unteren
Suchfeld ein und klicken Sie auf "Search".<p>
<img src="img/en_online_search.png"><p>
</blockquote>
