Come accettare un nuovo paziente<p>
<font face="Verdana, Arial" size=2>
<b>1</b>
<ul>Selezionare "Accettazione" nel menu della finestra principale.
</ul>
<p>
<b>2</b>
<ul> Se si è fatto login precedentemente e si hanno i diritti di accesso sufficienti, apparirà
nella finestra principale il modulo di accettazione.<br>
		Se invece non si è ancora effettuato il login, verrà richiesto di inserire username e password. 
		Inserire username e password, poi selezionare il bottone <img src="../img/<?php echo "$lang/$lang" ?>_continue.gif" border=0>.
</ul>
<p>
<b>3</b>
<ul>Inserire i dati personali del paziente, poi selezionare il bottone <input type="button" value="Salva">
		per salvare i dati.
		Se mancano informazioni, apparirà un messaggio che chiede di inserire i dati mancanti
		nei campi segnati in rosso. E' possibile forzare il salvataggio di dati incompleti selezionando
		il bottone <input type="button" value="Salva comunque">.
</ul>
<p>
<b>4</b>
<ul>Premere "Accettazione" nel riquadro a sinistra.
</ul>
