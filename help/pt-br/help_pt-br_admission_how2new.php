<b>Passo 1</b>

<ul> 
<font color=#ff0000>Primeiro verifique se os dados básicos do paciente já existem</font>.<p>
		 Entre  com uma informação completa ou umas poucas letras de informação do paciente, como por exemplo seu primeiro nome, ou sobrenome, 
		ou o PID (identificador de pessoa).
		<p>Exemplo 1: entre "21000012" ou "12".
		<br>Exemplo 2: entre "Guerero" ou "gue".
		<br>Exemplo 3: entre "Alfredo" ou "Alf".
		
</ul>

<b>Passo 2</b>
<ul> Clique no botão <img <?php echo createLDImgSrc('../','searchlamp.gif','0') ?>> para iniciar a pesquisa. Sua pesquisa será direcionada ao
modulo de pesquisa de pessoa.
</ul>

<b>Passo 3</b>
<ul> Se a pesquisa não encontrar nenhum resultado, você necesssita registrar a pessoa primeiro. Siga as instruções para registrar a pessoa.
</ul>
<b>Passo 4</b>
<ul> Se a pesquisa encontrar um resultado, selecione a pessoa certa da lista que for mostrada clicando no botão  <img <?php echo createLDImgSrc('../','ok_small.gif','0') ?>> que encontra-se ao lado.
</ul>
<b>Passo 5</b>
<ul> Com o formulário de admissão na tela, entre com todos os dados relevantes para a admissão.
		
</ul>
<b>Passo 6</b>
<ul> 
	 Clique no botão <img <?php echo createLDImgSrc('../','savedisc.gif','0') ?>> para salvar
		a informação de admissão.<p>
	
</ul>


<b>Nota</b>

<ul> Se faltar uma informação, os dados informados serão mostrados novamente e uma mensagem aparecerá solicitando
		que a informação seja digitada no campo ou campos marcados em vermelho. Após, clique no botão <img <?php echo createLDImgSrc('../','savedisc.gif','0') ?>> para
		salvar a informação completa.<p>
</ul>
<b>Nota</b>
<ul> Se você quiser salvar uma informação incompleta clique no botão <input type="button" value="Salve assim mesmo">.
</ul>
<b>Nota</b>
<ul> Se você decidir cancelar a admissão clique no botão <img <?php echo createLDImgSrc('../','cancel.gif','0') ?>>.
		
</ul>


