<li> relatório de enfermagem
<li> ordens do médico
<li> relatórios de diagnóstico
<li> módulo composto DRG 
<li> resultados de laboratório
<li> encontro de fotos/imagens
<li> teste requisição criação
</ul>

<font color="#990000"><b>
