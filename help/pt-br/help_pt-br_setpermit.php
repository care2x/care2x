<b>Configurando uma permissão de acesso</b><p>
Devido a requerimentos de alta segurança em sistemas de saúde, você não pode criar uma permissão de acesso completa por você. A permissão de acesso 
é geralmente criada para você por um colaborados designado pelo departamento de TI da sua organização.
<p>
<b>O que fazer?</b>
<p>
Solicite uma permissão de acesso através dos canais oficiais conforme orientado pelas regras de sua organização.
Você pode tentar as seguintes informações de contato:
<p>
<blockquote>
Nome da pessoa designada:<br>
Endereço do departamento:<br>
Número do telefone<br>
Número do Fax:<br>
Número de Intercom:<p>
Formulário de Solicitação
