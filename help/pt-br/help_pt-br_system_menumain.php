Como ocultar ou exibir um ítem do menu?</b></font>
<ul> 
	<b>Passo 1: </b>Desmarque a caixa de seleção "Visível" do menu para ocultar.<p>
	<b>Ou: </b>Marque a caixa de seleção "Visível" para exibir.<p>
	<b>Passo 2: </b>Clique no botão <img <?php echo createLDImgSrc('../','savedisc.gif','0') ?>> .<p>
	<b>Nota: </b>Por padrão, todos os ítens do menu são visíveis.<p>
	<b>Nota: </b> Os seguintes ítens não podem ser ocultados:
	<ul>
	EDP<br>
	Login/Logout
	</ul>
</ul>


