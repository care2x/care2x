<font face="Verdana, Arial" size=3 color="maroon"><b>How to use the online manual</b></font>
<p>
<font face="Verdana, Arial" size=2>
The online manual is internet based. You can open it only if your system has an internet access or if your firewall allows you to access the internet.<p>
<b>How to open the online manual</b>
<blockquote>
Click the "Online manual" item on the left menu.
</blockquote>
<p>
<b>Finding help quickly</b>
<blockquote>
<li>Use the "Find Page" function of the online manual. Click the "FindPage" link on the lower left of the page.
<p>
<img src="img/en_online_findpage.png"><p>
<li>Enter a title of the page in the first entry field, then click the "Search" button.
<li>Or you can enter a keyword derived from your needed help (for example: "daily diet") in the second entry field, then click the "Search" button.<p>
<img src="img/en_online_search.png"><p>
</blockquote>
